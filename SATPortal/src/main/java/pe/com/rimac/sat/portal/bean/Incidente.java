package pe.com.rimac.sat.portal.bean;

public class Incidente {

	private int idewf;
	private int ideestwf;
	private String descestado;
	private int idewfdef;
	private String desctipoinci;
	private String desctarea;
	private String fecusu;
	private String fecatencion;
	private String feccierre;
	
	public int getIdewf() {
		return idewf;
	}
	public void setIdewf(int idewf) {
		this.idewf = idewf;
	}
	public int getIdeestwf() {
		return ideestwf;
	}
	public void setIdeestwf(int ideestwf) {
		this.ideestwf = ideestwf;
	}
	public String getDescestado() {
		return descestado;
	}
	public void setDescestado(String descestado) {
		this.descestado = descestado;
	}
	public int getIdewfdef() {
		return idewfdef;
	}
	public void setIdewfdef(int idewfdef) {
		this.idewfdef = idewfdef;
	}
	public String getDesctipoinci() {
		return desctipoinci;
	}
	public void setDesctipoinci(String desctipoinci) {
		this.desctipoinci = desctipoinci;
	}
	public String getDesctarea() {
		return desctarea;
	}
	public void setDesctarea(String desctarea) {
		this.desctarea = desctarea;
	}
	public String getFecusu() {
		return fecusu;
	}
	public void setFecusu(String fecusu) {
		this.fecusu = fecusu;
	}
	public String getFecatencion() {
		return fecatencion;
	}
	public void setFecatencion(String fecatencion) {
		this.fecatencion = fecatencion;
	}
	public String getFeccierre() {
		return feccierre;
	}
	public void setFeccierre(String feccierre) {
		this.feccierre = feccierre;
	}

}
